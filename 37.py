sides = int(input("Enter a number of sides: "))

if sides == 3:
    print("It's a triangle")
elif sides == 4:
    print("It's a square")
elif sides == 5:
    print("It's a rhombus")
elif sides == 6:
    print("It's a hexagon")
elif sides == 7:
    print("It's an eptagon")
elif sides == 8:
    print("It's an octagon")
elif sides == 9:
    print("It's a nonagon")
elif sides == 10:
    print("It's a decagon")
else:
     print("It's not valid. Insert a value between or equal to 3 and 10")
     exit()
    

          
